import { SETDEX_SV } from "@data/movesets"
import { Ability } from "@lib/model/ability"
import { MoveSet } from "@lib/model/moveset"
import { Pokemon } from "@lib/model/pokemon"
import { Regulation } from "@lib/types"

export function pokemonByRegulation(regulation: Regulation): Pokemon[] {
  return Object.keys(SETDEX_SV)
    .map(p => {
      return new Pokemon(p, {
        ability: new Ability(SETDEX_SV[p].ability),
        nature: SETDEX_SV[p].nature,
        item: SETDEX_SV[p].items[0],
        teraType: SETDEX_SV[p].teraType,
        evs: SETDEX_SV[p].evs,
        moveSet: new MoveSet(SETDEX_SV[p].moves[0], SETDEX_SV[p].moves[1], SETDEX_SV[p].moves[2], SETDEX_SV[p].moves[3])
      })
    })
    .filter(p => !bannedByRegulation[regulation].includes(p.displayNameWithoutSuffix))
}

